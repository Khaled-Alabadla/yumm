def build_po() -> None:
    po = polib.POFile()
    po.metadata = {
        "Project-Id-Version": "Yumm 1.0",
        "Report-Msgid-Bugs-To": "",
        "Language": "ar",
        "MIME-Version": "1.0",
        "Content-Type": "text/plain; charset=UTF-8",
        "Content-Transfer-Encoding": "8bit",
        "Plural-Forms": "nplurals=6; plural=n==0 ? 0 : n==1 ? 1 : n==2 ? 2 : n%100>=3 && n%100<=10 ? 3 : n%100>=11 && n%100<=99 ? 4 : 5;",
    }

    for msgid, msgstr in sorted(TRANSLATIONS.items()):
        if not msgstr:
            continue
        entry = polib.POEntry(msgid=msgid, msgstr=msgstr)
        po.append(entry)

    for (msgid, msgid_plural), (s0, s1) in PLURALS.items():
        entry = polib.POEntry(msgid=msgid, msgid_plural=msgid_plural)
        # Arabic uses 6 plural forms — fill all indices so gettext never falls back to English.
        entry.msgstr_plural = {0: s0, 1: s0, 2: s1, 3: s1, 4: s1, 5: s1}
        po.append(entry)

    PO_PATH.parent.mkdir(parents=True, exist_ok=True)
    po.save(str(PO_PATH))
    po.save_as_mofile(str(MO_PATH))
    print(f"Wrote {PO_PATH} ({len(po)} entries)")
    print(f"Wrote {MO_PATH}")


if __name__ == "__main__":
    build_po()
